    int total_failures = 0;
    
    printf("--- Running Unit Tests ---\n");
    
    printf("Testing Geometric Primitives...\n");
    total_failures += test_geometry();
    
    printf("Testing Delaunay Core Logic...\n");
    total_failures += test_delaunay_logic();

